# --- Initialize chat history ---
if "messages" not in st.session_state:
    st.session_state.messages = []

# --- Display previous messages ---
chat_container = st.container()
with chat_container:
    for msg in st.session_state.messages:
        bubble_class = "user-bubble" if msg["role"] == "user" else "assistant-bubble"
        emoji = "ğŸ§‘â€ğŸ’¼" if msg["role"] == "assistant" else "âœˆï¸"
        with st.chat_message(msg["role"]):
            st.markdown(
                f'<div class="chat-bubble {bubble_class}">{emoji} {msg["content"]}</div>'
                f'<div class="timestamp">{msg["time"]}</div>',
                unsafe_allow_html=True,
            )

# --- User input section ---
st.markdown("### ğŸ’¬ Chat with the AI Travel Agent")

with st.form(key="query_form", clear_on_submit=True):
    user_input = st.text_input("Your Message", placeholder="e.g. Plan a 7-day trip to Switzerland for skiing â„ï¸")
    submitted = st.form_submit_button("Send")

if submitted and user_input.strip():
    # Save user input
    st.session_state.messages.append({
        "role": "user",
        "content": user_input,
        "time": datetime.datetime.now().strftime("%H:%M"),
    })

    with st.spinner("ğŸŒ Thinking and planning your trip..."):
        try:
            payload = {"query": user_input}
            response = requests.post(f"{BASE_URL}/query", json=payload, timeout=120)

            if response.status_code == 200:
                answer = response.json().get("answer", "No answer returned.")

                markdown_response = f"""
                ## ğŸ—ºï¸ AI-Generated Travel Plan  
                **Generated:** {datetime.datetime.now().strftime('%Y-%m-%d %H:%M')}  
                **Created by:** Dhruv's Travel Agent  

                ---
                {answer}
                ---
                *Please verify all details, especially pricing, timings, and travel rules before booking.*
                """

                # Show assistant message
                with st.chat_message("assistant"):
                    st.markdown(
                        f'<div class="chat-bubble assistant-bubble">ğŸ§‘â€ğŸ’¼ {markdown_response}</div>'
                        f'<div class="timestamp">{datetime.datetime.now().strftime("%H:%M")}</div>',
                        unsafe_allow_html=True,
                    )

                # Save assistant reply
                st.session_state.messages.append({
                    "role": "assistant",
                    "content": markdown_response,
                    "time": datetime.datetime.now().strftime("%H:%M"),
                })

            else:
                st.error("âŒ Bot failed to respond: " + response.text)

        except Exception as e:
            st.error(f"âš ï¸ The response failed due to {e}")
